class Solution{   
public:
    int getPairsCount(int arr[], int n, int k) {
        int count = 0;
        unordered_map<int,int> hash_map;
        for(int i=0;i<n;i++){
            int value = k - arr[i];
            if(hash_map[value]){
                count += hash_map[value];
            }
            hash_map[arr[i]]++;
        }
        return count;
    }
};

